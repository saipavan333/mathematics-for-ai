@reg(2)
def d_2_3():
    cv=Canvas(560,300)
    cv.title(280,22,"The dot product measures alignment: a·b = ‖a‖‖b‖cos θ")
    F=Frame(cv,58,46,320,220,-0.4,4.6,-0.4,3.6)
    F.grid(range(0,5),range(0,4)); F.axes()
    O=F.P(0,0); a=(4,1); b=(2,3)
    t=(a[0]*b[0]+a[1]*b[1])/(a[0]**2+a[1]**2); foot=(t*a[0],t*a[1])
    cv.arrow(*O,*F.P(*a),stroke=PAL["blue"],w=2.6)
    cv.arrow(*O,*F.P(*b),stroke=PAL["green"],w=2.6)
    cv.line(*F.P(*b),*F.P(*foot),stroke=PAL["amber"],w=1.6,dash="4 3")
    cv.dot(*F.P(*foot),fill=PAL["amber"])
    la=m.hypot(*a); ua=(a[0]/la,a[1]/la)
    rb=(b[0]-foot[0],b[1]-foot[1]); lr=m.hypot(*rb); ur=(rb[0]/lr,rb[1]/lr)
    cv.right_angle(F.X(foot[0]),F.Y(foot[1]),-ua[0],ua[1],ur[0],-ur[1],s=9,stroke=PAL["amber"])
    cv.text(F.X(3.3),F.Y(1)+17,"a = (4, 1)",fill=PAL["blue"],size=11,weight="600",anchor="middle")
    cv.text(F.X(2)+4,F.Y(3),"b = (2, 3)",fill=PAL["green"],size=11,weight="600",anchor="start")
    cv.text(F.X(foot[0])+18,F.Y(foot[1])+16,"proj of b on a",fill=PAL["amber"],size=9.5,anchor="middle")
    tx=402
    cv.text(tx,118,"a·b = 4·2 + 1·3 = 11",anchor="start",fill=PAL["ink"],size=11,weight="600")
