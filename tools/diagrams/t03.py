@reg(3)
def d_3_4():
    cv=Canvas(560,300)
    cv.title(280,22,"Projection: nearest point in the subspace; the error is perpendicular")
    F=Frame(cv,58,46,330,222,-0.5,4.5,-0.5,3.6)
    F.grid(range(0,5),range(0,4)); F.axes()
    O=F.P(0,0); a=(3,1); b=(1,2.5)
    t=(a[0]*b[0]+a[1]*b[1])/(a[0]**2+a[1]**2); p=(t*a[0],t*a[1])
    cv.poly([F.P(0,0),F.P(4.3,4.3/3)],stroke=PAL["blue"],w=2.0)
    cv.text(F.X(4.05),F.Y(4.05/3)+15,"subspace = span(a)",fill=PAL["blue"],size=10.5,anchor="end")
    cv.arrow(*O,*F.P(*b),stroke=PAL["green"],w=2.5)
    cv.line(*F.P(*b),*F.P(*p),stroke=PAL["amber"],w=1.8,dash="4 3")
    cv.dot(*F.P(*p),fill=PAL["indigo"])
    la=m.hypot(*a); ua=(a[0]/la,a[1]/la); rb=(b[0]-p[0],b[1]-p[1]); lr=m.hypot(*rb); ur=(rb[0]/lr,rb[1]/lr)
    cv.right_angle(F.X(p[0]),F.Y(p[1]),ua[0],-ua[1],ur[0],-ur[1],s=11,stroke=PAL["amber"])
    cv.text(F.X(b[0])-8,F.Y(b[1]),"b",fill=PAL["green"],size=13,weight="700",anchor="end")
    cv.text(F.X(p[0])+6,F.Y(p[1])+18,"p = proj(b)",fill=PAL["indigo"],size=10.5,anchor="start",weight="600")
    cv.text((F.X(b[0])+F.X(p[0]))/2-24,(F.Y(b[1])+F.Y(p[1]))/2-2,"residual",fill=PAL["amber"],size=10,anchor="end")
    caption(cv,"Least squares = projection: p is the closest point of span(a) to b, so the error b−p ⊥ a.")
    return "3.4",cv.svg()
@reg(3)
def d_3_5():
    cv=Canvas(560,300)
    cv.title(280,22,"Gram–Schmidt turns a1, a2 into a perpendicular unit pair q1, q2")
    F=Frame(cv,58,46,330,222,-1.0,4.0,-0.6,3.4)
    F.grid(range(-0,4),range(0,4)); F.axes()
    O=F.P(0,0); a1=(3,1); a2=(1,2.5)
    l1=m.hypot(*a1); q1=(a1[0]/l1,a1[1]/l1)
    proj=a2[0]*q1[0]+a2[1]*q1[1]; pv=(proj*q1[0],proj*q1[1])
    u2=(a2[0]-pv[0],a2[1]-pv[1]); l2=m.hypot(*u2); q2=(u2[0]/l2,u2[1]/l2)
    cv.arrow(*O,*F.P(*a1),stroke=PAL["ink3"],w=1.5)
    cv.arrow(*O,*F.P(*a2),stroke=PAL["ink3"],w=1.5)
    cv.line(*F.P(*a2),*F.P(*pv),stroke=PAL["amber"],w=1.5,dash="4 3")
    cv.arrow(*O,*F.P(*q1),stroke=PAL["blue"],w=2.8)
    cv.arrow(*O,*F.P(*q2),stroke=PAL["green"],w=2.8)
    cv.right_angle(F.X(0),F.Y(0),q1[0],-q1[1],q2[0],-q2[1],s=12,stroke=PAL["axis"])
    cv.text(F.X(a1[0])+6,F.Y(a1[1])+2,"a1",fill=PAL["ink3"],size=10.5,anchor="start")
    cv.text(F.X(a2[0])+6,F.Y(a2[1]),"a2",fill=PAL["ink3"],size=10.5,anchor="start")
    cv.text(F.X(q1[0])+6,F.Y(q1[1])+12,"q1",fill=PAL["blue"],size=11,weight="700",anchor="start")
    cv.text(F.X(q2[0])-6,F.Y(q2[1])-6,"q2",fill=PAL["green"],size=11,weight="700",anchor="end")
    caption(cv,"Keep a1's direction (q1); strip a1 out of a2 to get q2 ⊥ q1. Same span, now orthonormal.")
    return "3.5",cv.svg()
