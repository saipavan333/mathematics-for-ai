@reg(9)
def d_9_5():
    cv=Canvas(620,262)
    cv.title(310,22,"Correlation: do two variables rise and fall together?")
    rng=np.random.default_rng(1)
    def panel(px,r,label,col):
        F=Frame(cv,px,50,168,168,-3,3,-3,3); F.axes()
        L=np.linalg.cholesky(np.array([[1,r],[r,1]]))
        P=(L@rng.standard_normal((2,130))).T
        for p in P:
            if abs(p[0])<3 and abs(p[1])<3: cv.circle(F.X(p[0]),F.Y(p[1]),1.8,fill=col)
        cv.text(px+84,42,label,fill=PAL["ink2"],size=10,weight="600")
    panel(32,0.85,"positive (ρ ≈ +0.85)",PAL["green"])
    panel(226,0.0,"none (ρ ≈ 0)",PAL["blue"])
    panel(420,-0.85,"negative (ρ ≈ −0.85)",PAL["red"])
    caption(cv,"ρ = +1 perfect up-together, −1 perfect opposite, 0 no linear link — but ρ only sees straight-line trends.")
    return "9.5",cv.svg()
