@reg(7)
def d_7_5():
    cv=Canvas(560,300)
    cv.title(280,22,"Expectation = the balance point of the distribution")
    F=Frame(cv,58,50,440,196,-0.3,6.3,-0.02,0.6)
    F.axes()
    f=lambda x:(x*x*np.exp(-x))/2 if x>0 else 0
    xs=np.linspace(0.02,6.2,120)
    cv.polygon([F.P(0,0)]+[F.P(x,f(x)) for x in xs]+[F.P(6.2,0)],fill=PAL["blueF"],opacity=0.85)
    cv.poly([F.P(x,f(x)) for x in xs],stroke=PAL["blue"],w=2.2)
    mean=3.0
    cv.line(F.X(mean),F.Y(0),F.X(mean),F.Y(f(mean)+0.05),stroke=PAL["red"],w=1.8,dash="4 3")
    bx=F.X(mean); by=F.Y(0)
    cv.polygon([(bx,by+2),(bx-10,by+17),(bx+10,by+17)],fill=PAL["red"])
    cv.text(F.X(mean)+6,F.Y(f(mean))-6,"E[X] = 3",fill=PAL["red"],size=11,weight="700",anchor="start")
    cv.text(F.X(2.0),F.Y(0.30),"peak ≠ mean",fill=PAL["ink3"],size=10,anchor="end")
    caption(cv,"E[X] = ∫ x·p(x) dx is where the density would balance on a fingertip — pulled toward the long tail.")
    return "7.5",cv.svg()
