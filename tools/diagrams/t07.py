@reg(7)
def d_7_1():
    cv=Canvas(560,300)
    cv.title(280,22,"An integral is area — approximate it with thin rectangles")
    F=Frame(cv,58,46,440,220,-0.2,4.4,-0.2,4.6)
    F.grid(range(0,5),range(0,5)); F.axes()
    f=lambda x:0.5+0.35*x+0.18*x*x
    a,b,n=0.5,3.5,6; w=(b-a)/n
    for i in range(n):
        xm=a+(i+0.5)*w; h=f(xm)
        cv.rect(F.X(a+i*w),F.Y(h),F.X(a+w)-F.X(a),F.Y(0)-F.Y(h),fill=PAL["blueF"],stroke=PAL["blue"],sw=1.0)
    cv.poly(F.curve(f,-0.2,3.85),stroke=PAL["indigo"],w=2.4)
    cv.text(F.X(2.0),F.Y(4.3),"area ≈ Σ f(xᵢ)·Δx",fill=PAL["blue"],size=11,weight="600")
    caption(cv,"Add up the rectangle areas; as Δx → 0 the sum becomes the exact integral ∫f(x)dx.")
    return "7.1",cv.svg()
@reg(7)
def d_7_2():
    cv=Canvas(560,300)
    cv.title(280,22,"The Fundamental Theorem: area grows at the rate of the height")
    F=Frame(cv,58,46,440,220,-0.2,4.6,-0.2,3.4)
    F.grid(range(0,5),range(0,4)); F.axes()
    f=lambda x:0.6+0.9*x-0.13*x*x
    x0=2.6
    xs=np.linspace(0,x0,40)
    cv.polygon([F.P(0,0)]+[F.P(x,f(x)) for x in xs]+[F.P(x0,0)],fill=PAL["blueF"],opacity=0.85)
    cv.poly(F.curve(f,-0.2,4.4),stroke=PAL["indigo"],w=2.4)
    cv.line(F.X(x0),F.Y(0),F.X(x0),F.Y(f(x0)),stroke=PAL["red"],w=1.8)
    cv.rect(F.X(x0),F.Y(f(x0)),F.X(x0+0.28)-F.X(x0),F.Y(0)-F.Y(f(x0)),fill=PAL["amberF"],stroke=PAL["amber"],sw=1.0)
    cv.text(F.X(1.2),F.Y(0.8),"A(x) = ∫₀ˣ f",fill=PAL["blue"],size=12,weight="700")
    cv.text(F.X(x0)+20,F.Y(f(x0))-6,"A′(x) = f(x)",fill=PAL["red"],size=11,weight="700",anchor="start")
    caption(cv,"Nudge x by dx and the shaded area gains a sliver f(x)·dx — so the derivative of area is the curve.")
    return "7.2",cv.svg()
@reg(7)
def d_7_3():
    cv=Canvas(560,300)
    cv.title(280,22,"Integration by parts = splitting one rectangle into two areas")
    F=Frame(cv,80,46,300,220,0,3.4,0,3.4)
    F.grid([0,1,2,3],[0,1,2,3]); F.axes(xlabel="u",ylabel="v")
    g=lambda u:0.35+0.26*u*u
    u1,u2=0.5,2.7; v1,v2=g(u1),g(u2)
    us=np.linspace(u1,u2,40)
    cv.polygon([F.P(u1,0)]+[F.P(u,g(u)) for u in us]+[F.P(u2,0)],fill=PAL["blueF"],opacity=0.85)
    cv.polygon([F.P(0,v1)]+[F.P(u,g(u)) for u in us]+[F.P(0,v2)],fill=PAL["greenF"],opacity=0.8)
    cv.rect(F.X(0),F.Y(v2),F.X(u2)-F.X(0),F.Y(0)-F.Y(v2),fill="none",stroke=PAL["ink3"],sw=1.1)
    cv.rect(F.X(0),F.Y(v1),F.X(u1)-F.X(0),F.Y(0)-F.Y(v1),fill="none",stroke=PAL["ink3"],sw=1.0)
    cv.poly([F.P(u,g(u)) for u in np.linspace(0.15,3.0,60)],stroke=PAL["indigo"],w=2.2)
    cv.text(F.X(1.7),F.Y(0.5),"∫ v du",fill=PAL["blue"],size=12,weight="700")
    cv.text(F.X(0.42),F.Y(2.3),"∫ u dv",fill=PAL["green"],size=12,weight="700",anchor="start")
    caption(cv,"The two shaded pieces fill the big rectangle: ∫u dv + ∫v du = uv — trade a hard integral for an easier one.")
    return "7.3",cv.svg()
