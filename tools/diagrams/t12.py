@reg(12)
def d_12_1():
    cv=Canvas(560,300)
    cv.title(280,22,"Entropy of a coin peaks at p = 0.5 — maximum uncertainty")
    F=Frame(cv,58,50,440,204,-0.02,1.02,-0.03,1.13)
    F.axes(xlabel="p",ylabel="H (bits)")
    H=lambda p:-(p*np.log2(p)+(1-p)*np.log2(1-p)) if 0<p<1 else 0
    xs=np.linspace(0.001,0.999,220)
    cv.poly([F.P(x,H(x)) for x in xs],stroke=PAL["indigo"],w=2.4)
    cv.dot(F.X(0.5),F.Y(1.0),fill=PAL["red"])
    cv.text(F.X(0.5),F.Y(1.0)-8,"1 bit (most surprise)",fill=PAL["red"],size=10.5,weight="700")
    cv.text(F.X(0.03),F.Y(0.06),"0 (certain)",fill=PAL["ink3"],size=9.5,anchor="start")
    cv.text(F.X(0.97),F.Y(0.06),"0 (certain)",fill=PAL["ink3"],size=9.5,anchor="end")
    caption(cv,"Surprise is −log p; entropy averages it. A fair coin = 1 bit; a rigged coin (p→0 or 1) tells you almost nothing.")
    return "12.1",cv.svg()
@reg(12)
def d_12_2():
    cv=Canvas(560,300)
    cv.title(280,22,"Cross-entropy: the bits you pay coding true p with model q")
    F=Frame(cv,58,52,360,190,-0.5,3.7,0,0.62)
    p=[0.5,0.3,0.15,0.05]; q=[0.25,0.25,0.25,0.25]; xs=[0,1,2,3]
    for x,pv,qv in zip(xs,p,q):
        cv.rect(F.X(x)-25,F.Y(pv),23,F.Y(0)-F.Y(pv),fill=PAL["blueF"],stroke=PAL["blue"],sw=1.1)
        cv.rect(F.X(x)+2,F.Y(qv),23,F.Y(0)-F.Y(qv),fill=PAL["amberF"],stroke=PAL["amber"],sw=1.1)
        cv.text(F.X(x),F.Y(0)+15,"class %d"%x,fill=PAL["ink3"],size=8.5)
    F.axes()
    cv.rect(438,60,12,12,fill=PAL["blueF"],stroke=PAL["blue"]); cv.text(456,70,"true p",anchor="start",size=10,fill=PAL["ink2"])
    cv.rect(438,80,12,12,fill=PAL["amberF"],stroke=PAL["amber"]); cv.text(456,90,"model q",anchor="start",size=10,fill=PAL["ink2"])
    kl=sum(pv*np.log2(pv/qv) for pv,qv in zip(p,q))
    cv.text(438,124,"KL(p‖q)",anchor="start",fill=PAL["ink"],size=11,weight="700")
    cv.text(438,141,"= %.2f bits"%kl,anchor="start",fill=PAL["indigo"],size=11,weight="700")
    cv.text(438,164,"extra cost of",anchor="start",fill=PAL["ink3"],size=9.5)
    cv.text(438,178,"the mismatch",anchor="start",fill=PAL["ink3"],size=9.5)
    caption(cv,"H(p,q) = H(p) + KL(p‖q): the gap between true p (blue) and model q (amber) is the KL penalty you minimize.")
    return "12.2",cv.svg()
